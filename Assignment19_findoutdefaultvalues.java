package org_assignments;
/*WAP to find out default values of byte,short ,int, long,double,float,
boolean,char and string data types using global variable*/

public class Assignment19_findoutdefaultvalues 
{
	static int a;
	static byte b;
	static short c;
	static double e;
	static long e1;
	static float e2;
	static boolean e3;
	static char e4;
	static String e5;
	
	
	public static void main(String[] args) 
	{
		/* if declaration variable has static keyword in it at global 
		then we can directly access that variable by it's name i.e. see check the below line*/
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(e);
		System.out.println(e1);
		System.out.println(e2);
		System.out.println(e3);
		System.out.println(e4);
		System.out.println(e5);	
		
	}

}